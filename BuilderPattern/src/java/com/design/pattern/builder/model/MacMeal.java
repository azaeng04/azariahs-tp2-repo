package com.design.pattern.builder.model;

/**
 *
 * @author 210192461
 */
public class MacMeal {

    private String cooldrink;
    private String meal;
    private String sauce;
    private String typeService;
    private String hotDrink;
    private Boolean toys;
    private Boolean upsize;
    private Boolean chips;

    public String getCooldrink() {
        return cooldrink;
    }

    public void setCooldrink(String cooldrink) {
        this.cooldrink = cooldrink;
    }

    public String getMeal() {
        return meal;
    }

    public void setMeal(String meal) {
        this.meal = meal;
    }

    public String getSauce() {
        return sauce;
    }

    public void setSauce(String sauce) {
        this.sauce = sauce;
    }

    public String getTypeService() {
        return typeService;
    }

    public void setTypeService(String typeService) {
        this.typeService = typeService;
    }

    public String getHotDrink() {
        return hotDrink;
    }

    public void setHotDrink(String hotDrink) {
        this.hotDrink = hotDrink;
    }

    public Boolean getToys() {
        return toys;
    }

    public void setToys(Boolean toys) {
        this.toys = toys;
    }

    public Boolean getUpsize() {
        return upsize;
    }

    public void setUpsize(Boolean upsize) {
        this.upsize = upsize;
    }

    public Boolean getChips() {
        return chips;
    }

    public void setChips(Boolean chips) {
        this.chips = chips;
    }
}
